    {
        // list semua dokumen pembatalan
        $query = Pembatalan::byQuery(
            $r->get('q', ''),
            $r->get('from'),
            $r->get('to')
        );

        $paginator = $query
                    ->paginate($r->get('number'))
                    ->appends($r->except('page'));

        return $this->respondWithPagination($paginator, new PembatalanTransformer);
    }

